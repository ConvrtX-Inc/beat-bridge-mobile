/// Walk through model
class WalkThroughModel {
  /// Constructor
  WalkThroughModel(
      {this.index = 0,
      this.backgroundImagePath = '',
      this.logoImagePath = '',
      this.topHeaderText = '',
      this.bottomHeaderText = '',
      this.headerImagePath = '',
      this.buttonText = ''});

  /// Initialization for background image path
  final int index;

  /// Initialization for background image path
  final String backgroundImagePath;

  /// Initialization for logo image path
  final String logoImagePath;

  /// Initialization for top header text
  final String topHeaderText;

  /// Initialization for bottom header text
  final String bottomHeaderText;

  /// Initialization for header image path
  final String headerImagePath;

  /// Initialization for button text
  final String buttonText;
}
